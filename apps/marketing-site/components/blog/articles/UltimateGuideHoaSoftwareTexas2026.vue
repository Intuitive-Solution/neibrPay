    <h2 id="state-compliance">
      Texas compliance: what your software has to support
    </h2>

    <ul>
      <li>
        <strong>§209.0062 payment plans.</strong> At least three months, offered
        before foreclosure or further enforcement. The software must track the
        plan and apply payments correctly without piling on late fees that
        aren't allowed.
      </li>
      <li>
        <strong>§209.005 records inspection.</strong> 10 business days from a
        written request. Tagged document storage is non-negotiable.
      </li>
      <li>
        <strong>§209.006 / §209.007 notice and hearing.</strong> Written notice,
        opportunity to cure, and (for some fines) a hearing, timestamped and
        document-linked.
      </li>
      <li>
        <strong>§202.018 / §202.0181 management certificates.</strong>
        Each association has to file a management certificate with the county
        and TREC. The software should remind you when it needs updating (e.g.,
        new board members, new mailing address).
      </li>
      <li>
        <strong>§209.009 foreclosure prerequisites.</strong> Notice of
        delinquency, opportunity to cure, payment plan offer, all documented
        before a Rule 736 expedited foreclosure can proceed.
      </li>
      <li>
        <strong>§82.114 condo records (Ch. 82).</strong> If you're a
        condominium, additional record-keeping rules apply, the software should
        treat condo and HOA structures distinctly.
      </li>
      <li>
        <strong>Annual budget and disclosures.</strong> Many Texas governing
        documents require annual budget delivery to owners; the software should
        generate, send, and timestamp.
      </li>
    </ul>

