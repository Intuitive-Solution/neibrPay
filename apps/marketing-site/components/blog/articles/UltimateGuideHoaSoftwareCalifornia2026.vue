    <h2 id="must-have-features">
      8 must have features for California self managed HOAs
    </h2>

    <p>
      Most vendors list 50+ features. For a California self managed board under
      150 units, these are the eight that actually matter sized for Davis
      Stirling. If a tool is missing one, keep shopping.
    </p>

    <h3>1. Online dues collection (ACH and card, with §5658 awareness)</h3>
    <p>
      Civil Code §5658 lets owners pay assessments under protest while disputing
      them your platform needs to handle that without locking the owner out. ACH
      is non-negotiable for monthly assessments because it's the cheapest. Card
      handles the late paying owner who finally pays in November.
    </p>

    <h3>2. Special assessments under the §5605 cap</h3>
    <p>
      §5605 caps board-imposed special assessments at 5% of the budgeted gross
      expenses without a member vote. The platform should let you run a one-time
      levy with its own payment plan and reporting, separate from regular dues,
      and tag whether it required a vote.
    </p>

    <h3>3. Reserve study integration</h3>
    <p>
      §5550 requires a triennial reserve study. The platform should let you
      import the components, fund them, and forecast against actual
      contributions so the §5570 reserve summary disclosure isn't a year-end
      fire drill.
    </p>

    <h3>4. California friendly homeowner portal</h3>
    <p>
      Each owner gets a login showing balance, payment history, recorded
      governing documents, the latest financial, the reserve summary, and a way
      to submit ARC requests. The portal beats answering an email asking "what's
      our reserve study say?" for the fifth time.
    </p>

    <h3>5. Document storage with §5200 records-request tagging</h3>
    <p>
      Civil Code §5210 obligates you to make records available within 10
      business days for current records, 30 for prior fiscal years. Tagged
      document storage CC&amp;Rs, bylaws, rules, financials, minutes, reserve
      study, contracts, insurance turns a §5200 request into a click response.
    </p>

    <h3>6. California grade financial reporting</h3>
    <p>
      Income statements, balance sheets, accounts receivable aging, budget vs.
      actual, and the §5300 pro forma operating budget. Boards collecting
      $75k+/yr need a reviewed financial statement; $300k+ need an audit. The
      software should hand off cleanly to a California CPA.
    </p>

    <h3>7. Violations, fines, and ARC tracking</h3>
    <p>
      §5855 fining requires written notice, an opportunity to be heard, and a
      record. Photo upload, status workflow, audit log, and templated letters
      defensible enforcement, not a shouting match at the next meeting.
    </p>

    <h3>8. Mass communication and disclosure delivery</h3>
    <p>
      Email and SMS announcements with delivery timestamps that satisfy §5320
      individual notice requirements. The annual policy statement and budget
      report (§5300) need to go to every owner in writing every year and you
      need proof you sent it.
    </p>

