    <h2 id="state-compliance">
      California compliance: what your software has to support
    </h2>

    <p>
      This is where DIY spreadsheets fall apart. Your platform needs to make
      these obligations easy:
    </p>

    <ul>
      <li>
        <strong>§5300 annual budget report and policy statement.</strong>
        Pro forma budget, reserve summary, assessment schedule, insurance
        summary, FHA / VA certification status, IDR and ADR notices, and a
        statement of board policies all delivered to every owner 30 to 90 days
        before fiscal year end. The software should generate, send, and
        timestamp.
      </li>
      <li>
        <strong>§5200 records inspection.</strong> 10 business days for current
        records, 30 for prior fiscal years. Tagged document storage is
        non-negotiable.
      </li>
      <li>
        <strong>§5550 reserve study.</strong> Visual on site inspection at least
        every three years; annual board review of the funding plan.
      </li>
      <li>
        <strong>§5605 special assessment cap.</strong> 5% of budgeted gross
        expenses without a member vote. The software should flag when a proposed
        levy crosses the line.
      </li>
      <li>
        <strong>§5658 pay under protest.</strong> Owners can pay disputed
        assessments under protest while pursuing IDR / ADR. The platform has to
        record the protest without forcing the owner into delinquency.
      </li>
      <li>
        <strong>§5910.1 IDR.</strong> Internal dispute resolution must be
        offered before enforcement. Notice, response, meeting, and outcome all
        need a paper trail.
      </li>
      <li>
        <strong>§4040 / §4041 owner addresses.</strong> Each owner gets to
        designate a delivery address annually. Your platform's owner record must
        capture both the property address and the delivery address.
      </li>
      <li>
        <strong>SB 326 inspection retention (condos, 9 year cycle).</strong>
        Reports retained for two cycles, with the next inspection date flagged.
      </li>
    </ul>

