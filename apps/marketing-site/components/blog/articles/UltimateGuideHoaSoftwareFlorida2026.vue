    <h2 id="state-compliance">
      Florida compliance: what your software has to support
    </h2>

    <p>
      This is the section other state guides won't write, because it's
      Florida-specific and it's where DIY spreadsheets fall apart. Your platform
      needs to make these obligations easy:
    </p>

    <ul>
      <li>
        <strong>Chapter 720.303 official records.</strong> Owner records
        requests must be answered in 10 business days. Tagged document storage —
        recorded declaration, bylaws, rules, financials, minutes, contracts — is
        the difference between a one-click response and a fine.
      </li>
      <li>
        <strong>Annual budget &amp; financial report delivery.</strong> 720.303
        requires a year-end financial report; the format depends on revenue
        ($150k, $300k, $500k thresholds). The software should let you produce
        the right report and email it to all owners with a delivery timestamp.
      </li>
      <li>
        <strong>Reserve disclosures.</strong> Whether you fund reserves or waive
        (with the required member vote), the disclosures attached to the budget
        have to be in writing and on the record.
      </li>
      <li>
        <strong>SIRS for applicable buildings (Ch. 718).</strong> If your condo
        association is 3+ stories and 30+ years old, the SIRS-funded components
        need to be tracked, the inspection reports stored, and the reserve
        schedule reflected in your financials.
      </li>
      <li>
        <strong>Fining process audit trail.</strong> Notice, hearing opportunity
        before the fining committee, and the committee's vote — all timestamped
        and document-linked.
      </li>
      <li>
        <strong>Estoppel certificates.</strong> Sale of a unit triggers a 10
        business-day estoppel deadline (Ch. 720.30851). The software should
        generate the certificate from current ledger data, not from a CSV
        export.
      </li>
      <li>
        <strong>Sunbiz annual report.</strong> Software won't file it for you,
        but it should remind you in April so you don't lose corporate good
        standing on May 1.
      </li>
    </ul>

